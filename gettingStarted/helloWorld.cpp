
// This tells the program to use the iostream library
// iostream lets us do things like print text on the screen
#include <iostream>

// This line saves us from having to type 'std::' before standard commands like 'cout'
using namespace std;

int main()  // Every C++ program starts from the main function
{
    /* 
    cout is short for "character output" and it prints our insertions to the screen
    Here, it's going to print "Hello World!" when we run the program
    */
    cout << "Hello World!";
}

